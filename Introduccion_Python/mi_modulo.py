def suma(num1,num2):
    """parametro de entrada num int o float -> devuelve la suma de los parametros de entrada""" #!agreamos una descripcion para leer cuando llamamos a la funcion
    return num1 + num2
    

def resta(num1,num2):
    return num1 - num2

def mult(num1,num2):
    return num1 * num2